import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const webRoot = path.resolve(__dirname, '..');
const publicDir = path.join(webRoot, 'public');
const outputFile = path.join(webRoot, 'src', 'generated', 'media-manifest.js');

const IMAGE_EXTENSIONS = new Set(['.jpg', '.jpeg', '.png', '.webp', '.gif', '.avif', '.svg']);
const VIDEO_EXTENSIONS = new Set(['.mp4', '.mov', '.webm', '.m4v']);
const MEDIA_EXTENSIONS = new Set([...IMAGE_EXTENSIONS, ...VIDEO_EXTENSIONS]);

function ensureDir(dirPath) {
  if (!fs.existsSync(dirPath)) {
    fs.mkdirSync(dirPath, { recursive: true });
  }
}

function isMediaFile(fileName, extensions) {
  return extensions.has(path.extname(fileName).toLowerCase());
}

function sortNatural(a, b) {
  return a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' });
}

function toWebPath(absolutePath) {
  const rel = path.relative(publicDir, absolutePath).split(path.sep).map(encodeURIComponent).join('/');
  return `/${rel}`;
}

function getFilesFromDir(dirPath, allowedExtensions) {
  if (!fs.existsSync(dirPath)) return [];

  return fs
    .readdirSync(dirPath, { withFileTypes: true })
    .filter((entry) => entry.isFile() && isMediaFile(entry.name, allowedExtensions))
    .map((entry) => path.join(dirPath, entry.name))
    .sort((a, b) => sortNatural(path.basename(a), path.basename(b)));
}

function getImageFiles(dirPath) {
  return getFilesFromDir(dirPath, IMAGE_EXTENSIONS);
}

function getVideoFiles(dirPath) {
  return getFilesFromDir(dirPath, VIDEO_EXTENSIONS);
}

function stripExtension(fileName) {
  const ext = path.extname(fileName);
  return ext ? fileName.slice(0, -ext.length) : fileName;
}

function titleFromFileName(fileName) {
  return stripExtension(fileName).replace(/[_-]+/g, ' ').replace(/\s+/g, ' ').trim();
}

function getProjectMedia() {
  const projectsRoot = path.join(publicDir, 'freelanceprojects');
  const projectMedia = {};

  if (!fs.existsSync(projectsRoot)) return projectMedia;

  const projectDirs = fs
    .readdirSync(projectsRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort(sortNatural);

  for (const dirName of projectDirs) {
    const absDir = path.join(projectsRoot, dirName);

    const imageFiles = getImageFiles(absDir).filter(
      (filePath) => !path.basename(filePath).toLowerCase().includes('logo')
    );
    const videoFiles = getVideoFiles(absDir);

    projectMedia[dirName] = {
      images: imageFiles.map(toWebPath),
      videos: videoFiles.map((filePath) => ({
        title: titleFromFileName(path.basename(filePath)),
        url: toWebPath(filePath)
      }))
    };
  }

  return projectMedia;
}

function getHighlights() {
  const highlightsRoot = path.join(publicDir, 'image', 'highlights');
  const categories = [];

  if (!fs.existsSync(highlightsRoot)) return categories;

  const dirs = fs
    .readdirSync(highlightsRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort(sortNatural);

  for (const dirName of dirs) {
    const absDir = path.join(highlightsRoot, dirName);
    const images = getImageFiles(absDir).map(toWebPath);

    if (images.length === 0) continue;

    categories.push({
      key: dirName,
      title: dirName.toUpperCase(),
      thumbnail: images[0],
      images
    });
  }

  return categories;
}

function generateMediaManifest() {
  const heroImages = getImageFiles(path.join(publicDir, 'image', 'hero')).map(toWebPath);
  const galleryImages = getImageFiles(path.join(publicDir, 'image', 'gallery')).map(toWebPath);
  const highlights = getHighlights();
  const projects = getProjectMedia();

  const manifest = {
    generatedAt: new Date().toISOString(),
    heroImages,
    galleryImages,
    highlights,
    projects
  };

  ensureDir(path.dirname(outputFile));

  const fileContent = `// This file is auto-generated by tools/generate-media-manifest.js\nexport default ${JSON.stringify(
    manifest,
    null,
    2
  )};\n`;

  fs.writeFileSync(outputFile, fileContent, 'utf8');
  return manifest;
}

const isDirectRun = process.argv[1] && path.resolve(process.argv[1]) === __filename;

if (isDirectRun) {
  const manifest = generateMediaManifest();
  const totalProjectImages = Object.values(manifest.projects).reduce((sum, p) => sum + p.images.length, 0);
  const totalProjectVideos = Object.values(manifest.projects).reduce((sum, p) => sum + p.videos.length, 0);

  console.log(
    `[media-manifest] generated hero=${manifest.heroImages.length} gallery=${manifest.galleryImages.length} highlights=${manifest.highlights.length} projectImages=${totalProjectImages} projectVideos=${totalProjectVideos}`
  );
}

export { generateMediaManifest };
